package org.arch.model;

public class TrackModel {
	private int xPos = 1000;
	private int yPos = 2000;
	private int zPos = 3000;
	private boolean validity = true;
	
	public int getxPos() {
		return xPos;
	}
	public int getyPos() {
		return yPos;
	}
	public int getzPos() {
		return zPos;
	}
	public boolean getValidity() {
		return validity;
	}
	
	
}
